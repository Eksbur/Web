<h3><a>PlayStation 2 Slim</a></h3>
</header>
<p>
<hr>
С июля 2007 года Sony начала поставлять новую редакцию тонкой PlayStation 2 Slim — V16 (SCPH-79000) весом в 600 граммов 
(по сравнению с 900 граммов в модели SCPH-77001); вес блока питания был также уменьшен до 250 граммов (350 гр в предыдущей редакции). 
Следующая редакция V17 (SCPH-90000) была анонсирована к продаже в Японии с 22 ноября 2007 года, с пересмотренным внутренним дизайном, в 
который на этот раз включен блок питания, в результате чего общий вес консоли составил 720 граммов.
</p>
