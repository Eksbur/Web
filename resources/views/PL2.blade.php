<h3><a>Самая популярная</a></h3>
</header>
<p>
<hr>
Игровая приставка, ставшая наиболее быстро продаваемой и самой популярной игровой консолью в истории. 
На третий квартал 2011 года в мире продано почти 155 миллионов экземпляров PS2.
</p>
