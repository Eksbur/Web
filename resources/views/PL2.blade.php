<h3><a>Вертикальность</a></h3>
</header>
<p>
<hr>
Sony создала свою PS2 так, что геймеры могут выбирать, как им удобнее держать консоль – 
горизонтальное положение дарит классическое ощущение игры на консоли, в то время как вертикальное превращает девайс в небольшой настольный ПК.
Выбор полностью зависит от эстетического чувства пользователя, поскольку система хорошо работает в любом положении. Маленький нюанс, 
но он радует геймеров, и приятно думать, что PS2 первая из игровых консолей предложила такой вариант.
</p>
