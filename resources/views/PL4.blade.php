<h3><a>PlayStation VR</a></h3>
</header>
<p>
PlayStation VR представляет собой систему виртуальной реальности, работающую как надстройка над PlayStation 4. 
На этапе разработки система была известна как Project Morpheus; компания Sony впервые представила её публике на 
конференции Game Developers Conference в 2014 году[104][105]. Уже под названием PlayStation VR система официально 
поступила в продажу в октябре 2016 года, получив совместимость со всеми ревизиями и версиями PlayStation 4.
В комплект PlayStation VR входит гарнитура (шлем виртуальной реальности) со 
встроенным дисплеем, рассчитанным на разрешение 1080p, и процессорный модуль, 
предназначенный для обработки 3D-аудио и некоторых других функций, а также кабели для подключения к приставке и, при 
необходимости, внешнему экрану. Гарнитура оборудована светодиодами; во время работы системы подключённая к приставке камера 
отслеживает положение движение этих светодиодов и, как следствие, головы игрока. PlayStation VR также может использоваться совместно 
с чувствительными к движению контроллерами PlayStation Move.
</p>
