<h3><a>История разработки</a></h3>
</header>
<p>
По словам Марка Черни, главного архитектора консоли, её разработка началась в 2008 году. Черни, даже возглавляя 
разработку консоли, не являлся сотрудником компании Sony, а выступал как независимый подрядчик; он получил место руководителя разработки, 
напрямую обратившись к высокопоставленным руководителям Sony Филу Харрисону  и Масе Чатани, и получил их согласие. 
Черни уже в 2007 году знал, что разработчики игр испытывают проблемы в работе с PlayStation 3, и проводил опросы по специальной анкете, 
осторожно выясняя, что разработчики предпочли бы видеть в консоли следующего поколения. Среди этих предпочтений звучало желание иметь общую
оперативную память — в отличие от PlayStation 3, у которой было два раздельных набора оперативной памяти по 256 мегабайт, один основной и один для
графического процессора — а также «не больше восьми ядер».
</p>
<p>
Экзотическая микропроцессорная архитектура Cell, использованная в PlayStation 3, 
создавала сложности для разработчиков игр — таким образом, отправной точкой для разработчиков PlayStation 4 стал принцип «не превращать аппаратное 
обеспечение в головоломку, которую нужно сначала разгадать, чтобы создать качественную игру». Вместо Cell Черни стал изучать широко применяемую в 
персональных компьютерах архитектуру процессора x86, в конечном счёте положенную в основу PlayStation 4.
</p>
