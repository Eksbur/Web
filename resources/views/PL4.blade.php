<h3><a>DualShock 4</a></h3>
</header>
<p>
<hr>
DualShock 4 — новая версия контроллера семейства DualShock, совместимая c Playstation 4 и частично с Playstation 3. 
Контроллер оснащен тачпадом, гироскопом, акселерометром, возможностью вибрации, подсветкой, моно-спикером, микро-USB портом, выходом
для наушников, портом расширения и кнопкой Share, позволяющей быстро размещать запись игрового процесса и скриншоты в сети Интернет. 
Возможность размещения изображений и видео в сети могут быть ограничены/запрещены разработчиками для каждой отдельной игры. Возможность
использовать разные цвета подсветки позволяют игрокам по мнению компании более четко идентифицировать себя, а в играх, если разработчики посчитают нужным,
может использоваться в качестве дополнительных индикаторов, например, в Killzone: Shadow Fall цвет индикатора показывает состояние здоровья главного героя.
</p>
