<h3><a>PlayStation 4 Pro</a></h3>
</header>
<p>
<hr>
Модель PlayStation 4 Pro (CUH-7000), ранее известная под условным обозначением Neo, 
была представлена 7 сентября 2016 года и поступила в продажу во всем мире 10 ноября того же года. 
Она отличается как от первоначальной модели PlayStation 4, так и от Slim более совершенным аппаратным оснащением,
рассчитанным на разрешение 4K и улучшенную работу в системе виртуальной реальности PlayStation VR; 
усовершенствования включают в себя графический процессор мощностью 4.2 терафлопс и восьмиядерный центральный процессор повышенной частоты — 2,1 ГГц вместо 1,6 ГГц.
</p>
