<h2><a href="#">PlayStation 3</a></h2>
<h4><a href="#">“Вот это жизнь!”</a></h4>
</header>
<a href="#" class="image2"><img src="images/pl3.png" alt="" /></a>
<p>
Игровая приставка седьмого поколения, третья в семействе игровых систем PlayStation (не считая PlayStation Portable). 
С помощью PS3 можно играть, смотреть фильмы, слушать музыку, отправлять электронную почту и просматривать веб-страницы. 
С самого момента выпуска приставкой поддерживается большинство игр для консолей предыдущего поколения, PlayStation и PlayStation 2. 
Главными конкурентами консоли являются Xbox 360 от Microsoft и Wii от Nintendo.
Изначально выход PS3 планировался на 2005 год, но разработчики, которые имели доступ к версии приставки того времени, заявили, что
решение ещё слишком сырое (например, в той версии совсем не было GPU, его добавили в самом конце подготовки к запуску). 
Выход PS3 в Японии состоялся 11 ноября 2006 года, 17 ноября приставка появилась в Северной Америке в первом квартале 2007 года), 
в марте 2007 года — в Европе, а потом в России.
</p>
<p>
 В мае 2006 года на конференции Sony, прошедшей в рамках E3 2006, была озвучена информация о цене приставки, началах продаж, а также
был представлен новый игровой контроллер Sixaxis. Впервые PS3 была доступна для тестирования на TGS 2006, где посетители могли оценить несколько игр на новой приставке.
</p>
