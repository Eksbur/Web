<h3><a>Технические проблемы</a></h3>
</header>
<p>
Некоторые владельцы Sony PlayStation 3 столкнулись с так называемой проблемой жёлтого огня смерти (Yellow Light Of Death, YLOD).
В большей части это коснулось владельцев игровых приставок первых ревизий, именно на приставках с жёстким диском 60 Гб чаще 
проявлялась проблема перегрева. В новых релизах игровой приставки с жёстким диском 40 Гб и в версии Slim проблема перегрева встречается значительно реже.
В случае возникновения этой проблемы при включении консоли индикатор питания кратковременно светится жёлтым, а затем мигает красным.
Причиной проблемы (как и RROD у Xbox 360) обычно является нарушение пайки графического процессора RSX. При возникновении данной технической неисправности есть несколько вариантов её решения.
Также, часто на FAT-моделях выходит из строя BD-привод.
18-19 июня 2013 было выпущено обновление 4.45, которое стало причиной многочисленных отказов PS3 с з
аменёнными жёсткими дисками. По данным Forbes, больше всего отказов зафиксировано у приставок, в
которых заводские диски были заменены на диски объёмом от 500 ГБ до 1 ТБ.
</p>
