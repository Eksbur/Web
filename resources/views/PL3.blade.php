<h3><a>История</a></h3>
</header>
<p>
После выхода популярных игровых приставок PlayStation (1994 год) и PlayStation 2 (2000 год) создание новой модели было вопросом времени. 
Первые слухи о консоли следующего поколения появились в 2004 году, однако конкретных сведений Sony не давала до весны 2005 года.
Впервые PS3 была официально представлена 16 мая 2005 года в ходе конференции Sony на выставке Electronic Entertainment Expo (E3).
На выставке находился только макет, предварительную рабочую версию приставки компания представила на Tokyo Game Show (TGS) 2005 года. 
</p>
<p>
В мае 2006 года на конференции Sony, прошедшей в рамках E3 2006, была озвучена информация о цене приставки, началах продаж, а также
был представлен новый игровой контроллер Sixaxis. Впервые PS3 была доступна для тестирования на TGS 2006, где посетители могли оценить несколько игр на новой приставке.
</p>
