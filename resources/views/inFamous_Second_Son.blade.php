<h2><a>Разработка</a></h2>
</header>
<p>
ействие игры происходит в 2018 году, через семь лет после Infamous 2. В той части главным героем был Коул Макграт. 
Предыдущая часть закончилась тем, что Коул пожертвовал собой, чтобы вылечить человечество от Лучевой Чумы и уничтожить
Зверя. Коул использовал луч Ингибитора, который убивает его и других Проводников. После тех событий, Правительство
США создаёт ДЕЗ (Департамент Единой Защиты), чтобы выследить и захватить оставшихся в мире Проводников, которых называют «биотеррористами». 
Главным героем в этот раз является Делсин Роу (Трой Бэйкер), 24-летний художник граффити и член индийского племени «акомиш». 
Делсин имеет уникальную способность Проводника, которая позволяет ему поглощать силы других Проводников. Его брат, Реджи (Трэвис Уиллингэм),
является местным шерифом и часто арестовывает Делсина за его акты вандализма. Оба являются акомишами, территория которых находится на берегу
залива Салмон-Бэй, штат Вашингтон.
</p>
<p>
<a href="#" class="image"><img src="images/gifinf4.gif" alt="" /></a>
Главным антагонистом игры стала Августина Брук (Кристин Данфорд), директор Департамента Единой Защиты и 
Проводник, использующий в качестве своей силы бетон. Её действия в резервации Акомиш вынудили Делсина и Реджи отправиться в Сиэтл, который в
настоящее время находится под контролем ДЕЗа. В дальнейшем Делсину предстоит встретиться с тремя другими Проводниками: Генри «Хэнк» Дотри 
(Дэвид Штейнбора), который владеет «дымовой» способностью, Абигейл «Проныра» Уокер (Лаура Бэйли), бывшая наркоманка и убившая под наркотиками 
своего брата с помощью неона, и Юджин Симс (Алекс Уолш), геймер, владеющий «видео»-способностью.
</p>
