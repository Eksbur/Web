<h2><a>Вердикт</a></h2>
</header>
<p>
При игре в inFamous: Second Son невозможно не порадоваться как за Sucker Punch, сумевшую удовлетворить свои амбиции, 
так и за всех нас: новое поколение наконец наступило, а с ним грядут как минимум такие же захватывающие игры. Second 
Son не предлагает ничего кардинально нового, но выступает памятником тому, на что впредь стоит равняться. Сюжетно игра 
не ударяется в избитые клише про спасение мира, взамен рассказывая любопытную историю про молодого человека в непростой 
ситуации. Как под лучами осеннего солнца Сиэттла, так и под ночным дождем при тусклом свете фонарей, Делсин неоновой 
вспышкой несется вперед, оставляя позади лишь неспешно угасающие отпечатки себя из прошлого. Так и мы спешим навстречу всему новому.
</p>								
