<h1><a href="#">inFamous: Second Son</a></h1>
<h2><a>
"Человек нервничает в двух случаях: либо он трус, либо ему есть что скрывать."</a></h2>
</header>
<ahref="#" class="image"><img src="images/gifinf2.gif" alt="" /></a>
<p>
Компьютерная игра в жанре action-adventure с открытым миром, разработанная Sucker
Punch Productions и изданная Sony Computer Entertainment, вышедшая 21 марта 2014 
года для игровой консоли PlayStation 4. Игра является третьей в серии inFamous. Главным 
героем игры выступает Делсин Роу, обладающий сверхспособностями, которые он использует в 
бою и во время путешествий по Сиэтлу, который является местом действия событий игры. По
ходу игры Делсин приобретает новые способности и становится либо героем, либо злодеем, 
поскольку выборы игрока влияют на сюжет, концовку и на сами способности.
</p>
