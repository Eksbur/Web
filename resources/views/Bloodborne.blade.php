<h2><a>Сюжет</a></h2>
</header>
<p>
События игры разворачиваются в Ярнаме, средневековом городе, где бушует чума, превращающая местных жителей в 
обезумевших зверей. Опять же, как и в серии Souls, сценаристы не раскрывают всех карт сразу.
Главный герой, охотник, приходит в себя в полуразрушенной больнице, где ему переливает кровь неизвестный дед в 
инвалидном кресле (отличная идея для татуировки). После серии трипов герой оказывается на кладбище, где его
встречает говорящая кукла, изъясняющаяся туманными намеками. Сон ли это, явь – сразу и не скажешь.
</p>
<p>
<a href="#" class="image"><img src="images/gifblood3.gif" alt="" /></a>
Чтобы выяснить, чего это за чертовщина такая, нужно собирать крупицы информации и обращать внимание на мелочи.
Так, например, о церкви, в которой якобы можно найти спасение, вы узнаете из неприметной записки. Постучавшись в
случайную дверь, вы встретите человека, который расскажет, что в городе началась охота, и, чтобы покончить с ней,
нужно перебить всех монстров. Такие дела, за ручку тут не водят.
</p>
<header>
<a href="#" class="image"><img src="images/gifblood4.gif" alt="" /></a>
<h2><a>Вердикт</a></h2>
</header>
<p>
Bloodborne ощущается целостной, подчиненной одной задумке и самодостаточной. 
Ее не стоит ставить ниже или выше любой части серии Souls — она как бы рядом. 
Словно иная картина, написанная в той же технике и тем же мастером.
Миядзаки продолжает свой труд, поднимаясь на гору и вновь возвращаясь к ее подножию.
Каждая попытка приносит что-то новое. И пока путь до вершины не окажется пройден, поклонники 
его творчества будут идти рядом, надеясь разделить триумф.
</p>								
