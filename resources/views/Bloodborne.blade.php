<h1><a href="#">Bloodborne</a></h1>
<h2><a>
"Мы рождаемся из крови, воспитываемся кровью, погибаем от крови. 
Нам надо открыть глаза... Бойся древней крови".</a></h2>
</header>
<ahref="#" class="image"><img src="images/gifblood1.gif" alt="" /></a>
<p>
Компьютерная игра в жанре Action/RPG, разработанная совместно японскими компаниями 
From Software и SCE Japan Studio эксклюзивно для игровой приставки PlayStation 4 под 
руководством геймдизайнера Хидэтаки Миядзаки. Выпуск игры состоялся 24 марта 2015 года.
Действие игры начинается в охваченном эпидемией чумы вымышленном городе Ярнам, созданном в викторианско-готическом стиле. 
Игрок принимает управление за паломника, который прибыл в город для излечения своей болезни, но стал так называемым «охотником»
вследствие местного ритуала переливания крови. Сюжет игры, подобно предыдущим играм FromSoftware, не объясняется напрямую, 
поэтому одной из основных целей игрока остаётся исследование игровых локаций и уничтожение «добычи» — боссов.
</p>
